# Type casting - Implicit & Explicit Type Casting.
var1 = "10"
var2 = "5"

print(var1 + var2 + "\n")   # because both variables are string so concate each other

print(int(var1) + int(var2))    # Str to Int (Explicit TYPE CASTING)

"""
TYPES OF Explicit Type conversion IN PYTHON
str(), int(), float(), ord(), hex(), oct(), list(), tuple(), dict(), set()
"""

varX = 2        # int DataType
varY = 5.5      # float DataType
print(varX + varY)              # outcome in float (Implicit TYPE CASTING)